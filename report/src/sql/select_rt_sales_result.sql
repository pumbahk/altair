/* For dumping the data in rt_sales */

select
    perf_id as '公演ID',
    perf_name as '公演名',
    start_date as '公演開始日',
    ifnull(end_date, '設定なし') as '公演終了日',
    account_name as '配券元',
    event_id as 'イベントID',
    event_name as 'イベント名',
    total_sales as '売上',
    ticket_count_sum as '販売枚数（合計）',
    order_count_sum as '予約件数（合計）',
    ifnull(order_count_creditcard, 0) as '予約件数（クレカ支払）',
    ifnull(order_count_sej_payment, 0) as '予約件数（セブン支払）',
    ifnull(order_count_famiport_payment, 0) as '予約件数（ファミマ支払）',
    ifnull(order_count_rakuten_payment, 0) as '予約件数（楽天あんしん支払）',
    ifnull(order_count_reserve_payment, 0) as '予約件数（窓口支払）',
    ifnull(order_count_shipping_delivery, 0) as '予約件数（郵送受取）',
    ifnull(order_count_sej_delivery, 0) as '予約件数（セブン受取）',
    ifnull(order_count_famiport_delivery, 0) as '予約件数（ファミマ受取）',
    ifnull(order_count_reserve_delivery, 0) as '予約件数（窓口受取）',
    ifnull(order_count_qr_delivery, 0) as '予約件数（QR認証）',
    ifnull(order_count_orion_delivery, 0) as '予約件数（イベントゲート）',
    transaction_fee_sum as '決済手数料（合計）',
    ifnull(transaction_fee_creditcard, 0) as '決済手数料（クレカ支払）',
    ifnull(transaction_fee_sej, 0) as '決済手数料（セブン支払）',
    ifnull(transaction_fee_famiport, 0) as '決済手数料（ファミマ支払）',
    ifnull(transaction_fee_rakuten, 0) as '決済手数料（楽天あんしん支払）',
    ifnull(transaction_fee_reserve, 0) as '決済手数料（窓口支払）',
    delivery_fee_sum as '引取り手数料（合計）',
    ifnull(delivery_fee_shipping, 0) as '引取り手数料（郵送受取）',
    ifnull(delivery_fee_sej, 0) as '引取り手数料（セブン受取）',
    ifnull(delivery_fee_famiport, 0) as '引取り手数料（ファミマ受取）',
    ifnull(delivery_fee_reserve, 0) as '引取り手数料（窓口受取）',
    ifnull(delivery_fee_qr, 0) as '引取り手数料（QR認証）',
    ifnull(delivery_fee_orion, 0) as '引取り手数料（イベントゲート）',
    system_fee_sum as 'システム利用料（合計）',
    special_fee_sum as '特別手数料（合計）'
from rt_sales2016;